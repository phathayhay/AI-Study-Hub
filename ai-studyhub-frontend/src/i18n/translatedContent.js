export function getTranslatedCourseDesc(courseCode, defaultDesc = '', lang = 'vi') {
  const code = courseCode ? String(courseCode).trim().toUpperCase() : ''
  const entry = COURSE_TRANSLATIONS[code]
  if (entry && entry.desc && entry.desc[lang]) {
    return entry.desc[lang]
  }

  const lowerDesc = String(defaultDesc).toLowerCase()
  if (lowerDesc.includes('khoa học máy tính') || lowerDesc.includes('computer science')) {
    return COURSE_TRANSLATIONS.CSI104.desc[lang] || defaultDesc
  }
  if (lowerDesc.includes('toán rời rạc') || lowerDesc.includes('discrete mathematics')) {
    return COURSE_TRANSLATIONS.MAD101.desc[lang] || defaultDesc
  }
  if (lowerDesc.includes('xác suất') || lowerDesc.includes('statistics')) {
    return COURSE_TRANSLATIONS.MAS291.desc[lang] || defaultDesc
  }
  if (lowerDesc.includes('kỹ năng học tập') || lowerDesc.includes('student success')) {
    return COURSE_TRANSLATIONS.SSL101C.desc[lang] || defaultDesc
  }
  if (lowerDesc.includes('vovinam') || lowerDesc.includes('võ việt nam')) {
    return COURSE_TRANSLATIONS.VOV101.desc[lang] || defaultDesc
  }
  if (lowerDesc.includes('khởi nghiệp') || lowerDesc.includes('entrepreneurship')) {
    return COURSE_TRANSLATIONS.ENT101.desc[lang] || defaultDesc
  }
  return defaultDesc
}

